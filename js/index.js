// Inicialização das variáveis 'valorPrimario' e 'valorSecundario'
let valorPrimario = 40
let valorSecundario = 15

// Soma dos valores de 'valorPrimario' e 'valorSecundario'
console.log(valorPrimario + valorSecundario)  // Deve imprimir 55

// Subtração dos valores de 'valorPrimario' e 'valorSecundario'
console.log(valorPrimario - valorSecundario)  // Deve imprimir 25

// Divisão dos valores de 'valorPrimario' pelo valor de 'valorSecundario'
console.log(valorPrimario / valorSecundario)  // Deve imprimir aproximadamente 2.6666666666666665

// Multiplicação dos valores de 'valorPrimario' e 'valorSecundario'
console.log(valorPrimario * valorSecundario)  // Deve imprimir 600

// Cálculo do resto (módulo) da divisão de 'valorPrimario' por 'valorSecundario'
console.log(valorPrimario % valorSecundario)  // Deve imprimir 10
